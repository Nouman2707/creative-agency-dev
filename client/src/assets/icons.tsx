// function to get service icon by name
export const getServiceIcon = (iconName: string, size = 24, className = "") => {
  switch (iconName) {
    case 'PaintBrushIcon':
      return <PaintBrushIcon size={size} className={className} />;
    case 'DesktopIcon':
      return <DesktopIcon size={size} className={className} />;
    case 'CodeIcon':
      return <CodeIcon size={size} className={className} />;
    case 'ChartLineIcon':
      return <ChartLineIcon size={size} className={className} />;
    case 'CameraIcon':
      return <CameraIcon size={size} className={className} />;
    case 'VideoIcon':
      return <VideoIcon size={size} className={className} />;
    default:
      return <CodeIcon size={size} className={className} />;
  }
};
